package simulatorWindow.utils;

public class iVec3 {
    public float x;
    public float y;
    public float z;

    public iVec3() {
        x = 0; y = 0; z = 0;
    }

    public iVec3(float x, float y, float z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }
}
